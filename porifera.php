                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                        Porifera adalahalah satu jenis hewan yang secara fisik mirip dengan tumbuhan. Hewan ini memang memiliki bentuk mirip dengan tumbuhan. Karena alasan tersebut, makhluk hidup satu ini masih diperdebatkan hingga saat ini soal apakah termasuk hewan atau tumbuhan. Secara umum, porifera masuk dalam jenis kingdom Animalia, yakni hewan invertebrata atau tidak bertulang belakang, selomata (memiliki rongga tubuh), dan termasuk hewan multiseluler yang memiliki sifat primitif. Hewan porifera juga sering disebut sebagai hewan berpori atau spons. Sebab, sebagian besar kerangka tubuhnya terbuat dari serabut spongin. Mereka hidup di kedalaman hingga 8.500 di bawah permukaan laut. Tersebar di seluruh lautan, tercatat ada lebih dari 5.000 spesies spons porifera di lautan. Mereka menempel pada batu atau benda-benda di dalam air secara permanen.
                        </p>

