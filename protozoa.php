                                    <h4 class="mb-2">Aseksual</h4>
                                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                                        Tahap pembelahan menggunakan cara mitosis yang hasilnya akan terbentuk dua sel anakan. Dari beberapa jenis protozoa seperti halnya plasmodium akan melakukan proses reproduksi melalui cara aseksual dengan menggunakan skizogoni yang pada umumnya terjadi dibagian dalam sel inang, selanjutnya masing-masing bagian yang terdapat pada inti akan diusir menuju keluar secara bersamaan beserta bagian dari sitoplasma, sehingga pada akhirnya terbentuklah protozoa baru.
                                        </p>                                 
