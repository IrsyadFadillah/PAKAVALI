                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                        Protozoa adalah hewan bersel satu yang hidup sendiri atau dalam bentuk koloni/kelompok. Tiap Protozoa merupakan kesatuan yang lengkap, baik dalam susunan maupun fungsinya, sanggup melakukan semua fungsi kehidupan yang pada jasad lebih besar dilakukan oleh sel-sel khusus. Arti penting protozoa: Sebagai mata rantai penting dalam rantai makanan untuk komunitas dalam lingkungan akuatik. Contoh: zooplankton (hewan) hidup dari fitoplankton (tumbuhan) yang fotosintetik sebagai protozoa saprofitik dan protozoa pemakan bakteri.
                        </p>
                        <h2 class="mb-5">Ciri-Ciri Protozoa</h2>
                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                        Morfologi protozoa bervariasi, fisiologi dan metabolismenya disesuaikan dengan kebutuhan mereka; nutrisi adalah heterotrofik dalam bentuk parasit dan autotrofik yang hidup bebas, mereka memiliki siklus hidup yang lebih atau kurang kompleks, baik yang hidup bebas dan parasit, dan dalam banyak kasus, bentuk vegetatif (trophozoite) dan bentuk lain tahan (kista).
                        Bentuk dan ukuran protozoa sangat beragam. Beberapa berbentuk lonjong atau membola, ada yang memanjang, ada pula yang polimorfik (mempunyai berbagai bentuk morfologi pada tingkat tingkat yang berbeda dalam daur hidupnya). Beberapa protozoa berdiameter sekecil 1 nanometer; yang lain, seperti Amoeba proteus berukuran 600 nanometer atau lebih. Beberapa siliata yang umum mencapai ukuran 2.000 nanometer atau 2 mm, jadi dapat dilihat dengan mudah tanpa perbesaran. Berikut ini beberapa ciri-ciri lainnya dari protozoa yang perlu kamu ketahui:
                        </p>
