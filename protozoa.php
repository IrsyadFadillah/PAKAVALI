                        <h2 class="mb-5">Sistem Reproduksi Protozoa</h2>
                        <p class="lead mb-5 text-center" >Pada protozoa proses reproduksi terjadi secara seksual dan juga aseksual. Namun proses reproduksi yang sering dilakukan adalah melalui cara aseksual yakni pembelahan biner.</p>
                        <div class="container px-5 my-5">
                            <div class="row gx-5">
                                <div class="col-lg-6">
                                    <div class="position-relative mb-5">
                                        <h4 class="mb-2">Seksual</h4>
                                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                                        Proses reproduksi melalui cara seksual yang terjadi pada protozoa bisa terjadi dengan cara konjugasi, yang akan menggabungkan berbagai materi genetik yang diambil dari bagian sel satu ke bagian sel yang lainnya. Bisa juga dengan menggunakan gamet melalui bantuan orang lain untuk melakukan perkawinan, sehingga membentuk zigot. Proses reproduksi secara seksual yang terjadi pada plasmodium ialah nyamuk, zigot yang dihasilkan akan mengalami proses mitosis sehingga akan menghasilkan banyak sporozoit.
                                        </p>                                      
                                    </div>
                                </div>
