                        Plankton adalah organisme baik tumbuhan maupun hewan yang umumnya berukuran relatifkecil (mikro), hidup melayang-layang di air, tidak mempunyai daya gerak walaupun ada, daya gerak relatif lemah sehingga distribusinya sangat dipengaruhi oleh daya gerak air, seperti arus dan lainnya.
