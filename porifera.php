                                        <h2 class="fw-bolder">Calcarea</h2>
                                        <p class="lead fw-normal text-muted mb-0 text-justify">Dilansir dari Encyclopedia Britannica, anggota kelas ini merupakan para spons berkapur, di mana seluruh kerangkanya terdiri dari spikula kalsium karbonat (seperti jarum). Tubuhnya berbentuk tabung dengan ukurannya yang kecil sekitar 1-3 cm, jarang sekali ada yang mencapai 15 cm. Umumnya memiliki warna yang kusam dan hidup di dasar perairan yang dangkal dengan iklim sedang. Mereka ditemukan di bawah batu dengan daerah yang relatif terlindungi. Contoh hewan porifera anggota Calcarea: Sycon ciliatum dan Leucosolenia botryoides.</p>
