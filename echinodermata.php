                                    <div class="col-lg-6">
                                        <h2 class="fw-bolder">Echinoidea</h2>
                                        <p class="lead fw-normal text-muted mb-0 text-justify">Biasa juga disebut landak laut yang hidup di pasir dan bebatuan. Hewan ini tidak memiliki lengan, berbentuk bundar agak pipih, dan permukaan tubuhnya dikelilingi banyak duri. Mulut dan lima buah gigi hewan ini berada pada bagian oral. Sedangkan anus dan lubang kelamin ada di bagian aboral. Contohnya bulu babi dan landak laut.</p>
                                    </div>
                                </div>
                            </div>
