                                    <div class="col-lg-6">
                                        <h2 class="fw-bolder">Crinoidiea</h2>
                                        <p class="lead fw-normal text-muted mb-0 text-justify">Bentuknya mirip bunga lili (lili laut) dengan lima buah lengan yang bercabang. Hewan ini idup menempel pada substrat dan membentuk koloni, sehingga samar terlihat seperti taman laut. Jenis echinodermata ini tidak memiliki madreporit, sementara mulut dan anusnya terletak pada bagian oral. Pada bagian aboral terdapat kaliks, yaitu lempengan kapur berbentuk seperti cangkir. Contohnya Antedon sp dan Holopus sp.</p>
                                    </div>
                                </div>
                            </div>
                        </section>
