                                    <div class="col-lg-6">
                                        <h2 class="fw-bolder">Asteroida</h2>
                                        <p class="lead fw-normal text-muted mb-0 text-justify">Bentuk tubuhnya seperti bintang, hidup di habitat pesisir pantai. Memiliki lima lengan berduri pendek dan tumpul pada permukaan tubuhnya. Alat geraknya berupa kaki ambulakral. Tubuh bintang laut menjadi sisi oral atau sisi bawah (tempat mulut), dan sisi aboral atau sisi atas (tempat anus). Lapisan permukaan paling luarnya tersusun dari lapisan sel epidermis bersilia dan memiliki endoskeleton di bawahnya.</p>
                                    </div>
                                </div>
                            </div>
