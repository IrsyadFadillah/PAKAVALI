                                    <div class="col-lg-6">
                                        <h2 class="fw-bolder">Ophiuroidea</h2>
                                        <p class="lead fw-normal text-muted mb-0 text-justify">Biasa juga disebut bintang ular karena memiliki lengan panjang dan pergerakannya seperti ular. Habitatnya berada di perairan laut dalam dan dangkal, khususnya di balik batu karang atau terkubur dalam pasir. Hewan ini memiliki madreporit yang terletak pada bagian bawah (mulut), dan tidak memiliki anus, sehingga sisa makanan dikeluarkan lewat mulut.</p>
                                    </div>
                                </div>
                            </div>
                        </section>
