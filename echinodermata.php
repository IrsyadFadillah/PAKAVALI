                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                        Filum echinodermata merupakan hewan laut yang bergerak lamban dan hidup secara sesil atau melekat pada substrat. Sebagian besar filum ini berkulit tajam karena tonjolan rangka dan duri. Dari seluruh hewan invertebrata, kekerabatan echinodermata lebih dekat dengan chordata. Diperkirakan echinodermata telah hidup sebelum periode cambrian.
                        
                        <h2 class="mb-3">Karakteristik Filum Echinodermata</h2>
                        <div class="container px-5 my-5">
                            <div class="row gx-5">
                                <div>
                                    <div class="position-relative mb-5">
                                        <ul>
                                            <li>
                                                <p class="lead fw-normal text-muted mb-3 text-justify">
                                                Tubuhnya terbagi menjadi lima bagian, berbentuk bulat, silindris, atau seperti bintang.
                                                </p>
                                            </li>
                                            <li>
                                                <p class="lead fw-normal text-muted mb-3 text-justify">
                                                Tubuhnya tak bersegmen.
                                                </p>
                                            </li>
                                            <li>
                                                <p class="lead fw-normal text-muted mb-3 text-justify">
                                                Pada fase larva, simetri tubuhnya bilateral. Sedangkan saat dewasa, simetri tubuhnya radial.
                                                </p>
                                            </li>
                                            <li>
                                                <p class="lead fw-normal text-muted mb-3 text-justify">
                                                Bersifat triploblastik atau memiliki tiga lapisan tubuh. 
                                                </p>
                                            </li>
                                            <li>
                                                <p class="lead fw-normal text-muted mb-3 text-justify">
                                                Tidak memiliki kepala, namun memiliki rangka dalam (endoskeleton).
                                                </p>
                                            </li>
                                            <li>
                                                <p class="lead fw-normal text-muted mb-3 text-justify">
