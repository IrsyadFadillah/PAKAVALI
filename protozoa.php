                        <h2 class="mb-5">Klasifikasi Protozoa</h2>
                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">Klasifikasi makhluk hidup memudahkan kita dalam mempelajari suatu objek dengan mengelompokkan berdasarkan persamaan maupun perbedaan yang ada. Protozoa hidup di air atau setidaknya di tempat yang basah. Mereka umumnya hidup bebas dan terdapat di lautan, lingkungan air tawar, atau daratan. Beberapa spesies bersifat parasitik, hidup pada organisme inang. Inang protozoa yang bersifat parasit dapat berupa organisme sederhana seperti algae, sampai vertebrata yang kompleks, termasuk manusia. Beberapa spesies dapat tumbuh di dalam tanah atau pada permukaan tumbuh-tumbuhan. Semua protozoa memerlukan kelembaban yang tinggi pada habitat apapun. Mari kenali klasifikasi protozoa dalam empat kategori berikut ini:</p>
                        <div class="container px-5 my-5">
                            <div class="row gx-5">
                                <div class="col-lg-6">
                                    <div class="position-relative mb-5">
                                        <h4 class="mb-2">Rhizopoda</h4>
                                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                                        Rhizopoda atau Sarcodina (Rhizoid = akar, podos = kaki) yaitu protozoa yang bergerak dengan menggunakan pseudopodia (kaki semu) yang merupakan penjuluran dari sitoplasma, contohnya pada Amoeba, Foraminifera, Radiolaria, Arcella, Entamoeba coli, dan Entamoeba histolytica. Merupakan hewan mikroskopis yang hidup sebagai massa kecil bersifat amorf atau dapat berubah â€“ ubah bentuknya. Kelas rhizopoda dibagi menjadi 5 ordo yakni :
                                        <b>Ordo Lobosa</b>: Mempunyai pseudopodia pendek dan tumpul serta terdapat perbedaan yang jelas antara ektoplasma serta endoplasma;
                                        <b>Ordo Filose</b>: Mempunyai pseudopodia halus seperti benang dan becabang-cabang;
                                        <b>Ordo Foraminifera</b>: Mempunyai pseudopodia panjang dah halus;
                                        <b>Ordo Helioza</b>: Mempunyai pseudopodia berbentuk benang yang radien dan antarfilamen tidak pernah bersatu membentuk jala atau anyaman;
                                        <b>Ordo Radiolarian</b>: Mempunyai pseudopodia berupa benang-benang halus yang tersusun radier dan bercabang-cabang membentuk jala (anyaman).
                                        </p>                                      
                                    </div>
                                </div>
                                <div class="col-lg-6">
                                    <div class="position-relative mb-5">
                                    <h4 class="mb-2">Flagellata</h4>
                                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                                        Flagellata berasal dari kata flagellum yang berarti bulu cambuk. Jadi, organisme yang termasuk fillum Flagellata semuanya memiliki bulu cambuk. Fillum flagellata disebut juga mastigophora (mastix : bulu cambuk dan phoros : membawa). Flagel atau bulu cambuk selain sebagai alat gerak juga berfungsi untuk alat peraba dan alat penangkap makanan. Flagel juga berfungsi sebagai alat indera. Kelompok flagellata merupakan kelompok protozoa yang unik. Beberapa anggotanya memiliki klorofil sehingga ada yang mengelompokkannya ke dalam alga. Berdasarkan ada tidaknya klorofil, flagellata dibagi menjadi fitoflagellata dan zooflagellata. Memiliki dinding tubuh yang berupa pellicle, sehingga bentuknya relatif tetap dengan ukuran lebih kurang 0,1 mm. Memiliki inti dan pada beberapa species memiliki kloroplas dengan klorofilnya yang berfungsi untuk fotosintesis.
                                        </p>                                 
                                    </div>
                                </div>
                                <div class="col-lg-6">
                                    <div class="position-relative mb-5">
                                        <h4 class="mb-2">Sprozoa</h4>
                                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                                        Sporozoa memiliki tubuh sederhana berbentuk bulat panjang dengan sebuah nukleus. Tidak mempunyai alat gerak atau (bergerak dengan sel itu sendiri) maupun vakuola kontraktil. Disebut Sporozoa karena dalam tahap tertentu dalam hidupnya, dapat membentuk sejenis spora. Hampir semua anggota sporozoa adalah parasit.
                                        Sporozoa melakukan pembiakan secara vegetatif (aseksual) disebut juga Skizogoni dan secara generatif (seksual) disebut Sporogoni. Secara vegetative pembelahan berganda kemudian akan menghasilkan banyak anak baru. Secara generative yaitu melalui pergiliran keturunan antara fase vegetatif pada tubuh manusia dan fase generatif pada tubuh hospes perantara seperti Plasmodium dengan fase generative pada nyamuk Anopheles betina.
                                        </p>                                      
                                    </div>
                                </div>
                                <div class="col-lg-6">
                                    <div class="position-relative mb-5">
                                    <h4 class="mb-2">Ciliata</h4>
                                        <p class="lead mb-5 text-justify" style="text-indent: 0.5in">
                                        Memiliki bentuk relatif tetap dan bergerak dengan rambut getar atau disebut cilia. Memiliki inti dan beberapa species intinya lebih dari satu, contoh Paramecium aurelia. Hidup di tempat-tempat yang berair misal: sawah, rawa, tanah berair dan banyak mengandung bahan organik. Bagi yang hidup bebas terdapat vakuola kontraktil, sementara hewan parasit tidak ada.
                                        Respirasi dan ekskresi melalui permukaan tubuh. Pencernaan makanan secara internal pada vakuola makanan. Sedangkan cara menangkap makanan adalah dengan menggetarkan rambut (silianya), maka terjadi aliran air keluar dan masuk mulut sel. Saat itulah bersamaan dengan air masuk bakteri bahan organik atau hewan uniseluler lainnya. Anggota ciliata ada yang hidup bebas seperti Paramecium candatum, adapula yang hidup sebagai parasite seperti Nyctoterus ovalis dan Balantidium coli.
                                        </p>                                 
